export const getCategory = (query: string): string => {
  const q = query.toLowerCase();
  if (q.includes("аналитик") || q.includes("статистик") || q.includes("цифр") || q.includes("отчёт")) return "analytics";
  if (q.includes("команд") || q.includes("сотрудник") || q.includes("загрузк") || q.includes("эффективн")) return "team";
  if (q.includes("финанс") || q.includes("деньг") || q.includes("прибыл") || q.includes("бюджет") || q.includes("руб")) return "finance";
  if (q.includes("безопасн") || q.includes("доступ") || q.includes("сесси") || q.includes("парол")) return "security";
  if (q.includes("склад") || q.includes("остатк") || q.includes("материал") || q.includes("поставк")) return "stock";
  if (q.includes("регламент") || q.includes("инструкц") || q.includes("стандарт")) return "regulation";
  if (q.includes("адаптац") || q.includes("новин") || q.includes("наставн") || q.includes("стажировк")) return "onboarding";
  return "general";
};

export const generateMockResponse = (userText: string, currentUser: string, leadsCount?: number): string => {
  const cat = getCategory(userText);

  if (cat === "general") {
    if (userText.toLowerCase().includes("дедлайн") || userText.toLowerCase().includes("срок")) {
      if (leadsCount && leadsCount > 0) {
        return "Проверяю дедлайны... Есть просрочка по заказу Л/017/26 (дедлайн 3 дня назад!). Рекомендую форсировать утверждение по активным заказам, чтобы не накапливать отставание.";
      }
      return "Проверяю дедлайны... На данный момент просрочен заказ Л/017/26 (3 дня). Советую обратить внимание — подвинуть клиента или пересмотреть сроки.";
    }
    if (userText.toLowerCase().includes("привет") || userText.toLowerCase().includes("здравствуй")) {
      return `Здравствуйте, ${currentUser === "Администратор" ? "руководитель" : currentUser || "коллега"}! Я Борис, ваш цифровой штурман по Л-кам. Готов помочь с аналитикой заказов, контролем раскроя и остатками ЛДСП. Чем могу быть полезен?`;
    }
  }

  const responses = categoryResponses[cat] || categoryResponses.general;
  return responses[Math.floor(Math.random() * responses.length)];
};

export const generateLeadAnalysis = (lead: {
  id: string;
  name: string;
  type: string;
  material: string;
  budget: string;
  deadline: string;
  status: string;
}): string => {
  const risk =
    lead.budget && lead.budget !== "—"
      ? parseInt(lead.budget.replace(/\D/g, "")) < 5000
        ? "Бюджет ниже среднего — возможны риски по качеству ЛДСП. Рекомендую согласовать с клиентом декор."
        : parseInt(lead.budget.replace(/\D/g, "")) > 30000
          ? "Бюджет высокий — отличная маржинальность, рекомендую предложить кромку PVC 2мм."
          : "Бюджет адекватен, риски минимальны."
      : "Бюджет не указан — рекомендую уточнить на созвоне с клиентом.";

  const deadlines = lead.deadline
    ? new Date(lead.deadline) < new Date("2026-06-13")
      ? `⚠️ Дедлайн ${lead.deadline} ПРОШЁЛ! Требуется срочное согласование с клиентом.`
      : `Дедлайн ${lead.deadline} — заложить +3 дня на согласование.`
    : "Дедлайн не указан — уточнить на старте.";

  const statusAdvice: Record<string, string> = {
    new: "Лид новый — рекомендую как можно скорее провести расчёт в калькуляторе Л-ок и уточнить декор.",
    project: "Стадия утверждения — убедитесь, что клиент согласовал эскиз и переходите к заказу ЛДСП.",
    measure: "Заказ утверждён — проверьте сроки поставки ЛДСП и свяжитесь с производством.",
    production: "В сборке — контролируйте сроки изготовления.",
    mounting: "Готов к выдаче — подготовьте документы и чек для клиента.",
  };

  return (
    `Проанализировал заказ ${lead.id} (${lead.name}).\n\n` +
    `📋 **Тип**: ${lead.type}\n` +
    `🔧 **Материал**: ${lead.material}\n` +
    `💰 **Бюджет**: ${lead.budget || "не указан"}\n` +
    `📅 **Дедлайн**: ${lead.deadline || "не указан"}\n` +
    `📍 **Статус**: ${statusAdvice[lead.status] || "Статус не определён"}\n\n` +
    `📊 **Риски**: ${risk}\n\n` +
    `📌 **Рекомендация**: ${deadlines}`
  );
};
